t -> 新規タブでファイルを開く
E -> 垂直分割してファイルを開く
l -> フォルダを展開する、ファイルを開く
! -> 隠しファイルを表示/非表示する

N -> ファイルを新規作成する
K -> ディレクトリを新規作成する
D -> ファイルを削除する
m -> ファイルを移動する
R -> ファイル名を変更する
- -> ファイルを選択する
C -> ファイルをコピーする
P -> ペーストする
M -> ファイルをコピーし、ペーストした時に元のファイルを削除する
z -> ファイル名に合わせてウィンドウ幅を広げる
y -> ファイルパスをコピーする

ctrl+h -> 親ディレクトリに移動する
ctrl+m -> フォルダを開く
e -> ファイルを開く
j,k -> 上下に移動する
