j,k -> 上下に移動する
ctrl+h -> 親ディレクトリに移動する

l -> フォルダを展開する、ファイルを開く
ctrl+m -> フォルダを開く
e -> ファイルを開く
! -> 隠しファイルを表示/非表示する

E -> 垂直分割してファイルを開く
t -> 新規タブでファイルを開く

N -> ファイルを新規作成する
K -> ディレクトリを新規作成する
D -> ファイルを削除する
m -> ファイルを移動する
R -> ファイル名を変更する
- -> ファイルを選択する
C -> ファイルをコピーする
P -> ペーストする
M -> ファイルをコピーし、ペーストした時に元のファイルを削除する
y -> ファイルパスをコピーする
z -> ファイル名に合わせてウィンドウ幅を広げる
